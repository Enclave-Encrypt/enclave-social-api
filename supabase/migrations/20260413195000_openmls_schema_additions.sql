create policy "Users can read MLS commits for their groups"
on public.mls_commits
for select
to authenticated
using (
  exists (
    select 1
    from public.mls_group_members mgm
    where mgm.mls_group_id = public.mls_commits.mls_group_id
      and mgm.user_id = auth.uid()
  )
);

create policy "Users can insert MLS commits from their own devices"
on public.mls_commits
for insert
to authenticated
with check (
  exists (
    select 1
    from public.mls_group_members mgm
    join public.user_devices ud on ud.id = public.mls_commits.sender_device_id
    where mgm.mls_group_id = public.mls_commits.mls_group_id
      and mgm.user_id = auth.uid()
      and ud.user_id = auth.uid()
  )
);
