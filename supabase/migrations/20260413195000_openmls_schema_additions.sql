  )
);

create policy "Users can add MLS memberships from their own devices"
on public.mls_group_members
for insert
to authenticated
with check (
  exists (
    select 1
    from public.user_devices ud
    where ud.id = user_device_id
      and ud.user_id = user_id
  )
  and exists (
    select 1
    from public.user_devices ad
    where ad.id = added_by_device_id
      and ad.user_id = auth.uid()
  )
);

