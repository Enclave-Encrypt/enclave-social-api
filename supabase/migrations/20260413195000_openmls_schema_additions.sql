alter table public.mls_commits enable row level security;

create policy "Users can read MLS groups they belong to"
on public.mls_groups
for select
to authenticated
using (
  exists (
    select 1
    from public.mls_group_members mgm
    where mgm.mls_group_id = public.mls_groups.id
      and mgm.user_id = auth.uid()
  )
);

create policy "Users can create MLS groups from their own devices"
on public.mls_groups
for insert
to authenticated
with check (
  creator_device_id is null
  or exists (
    select 1
    from public.user_devices ud
    where ud.id = creator_device_id
      and ud.user_id = auth.uid()
      and ud.is_active = true
  )
);

create policy "Group members can update MLS groups"
on public.mls_groups
for update
to authenticated
using (
  exists (
    select 1
    from public.mls_group_members mgm
    where mgm.mls_group_id = public.mls_groups.id
      and mgm.user_id = auth.uid()
  )
)
with check (
  exists (
    select 1
    from public.mls_group_members mgm
    where mgm.mls_group_id = public.mls_groups.id
      and mgm.user_id = auth.uid()
  )
);

